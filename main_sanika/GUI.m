% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%
global NImg1
global A
global F_img
if A == 1
    %% Apply Median filter
    
    
    F_img2 = medfilt2(NImg1);
    
    % Apply modified wiener filter
    
    F_img1 = MMWF_2D(im2double(F_img2),5);
    
    % Apply Bilateral Filter
    
    F_img3 = im2double(F_img1);
    w     = 2;       % bilateral filter half-width
    sigma = [3 0.1]; % bilateral filter standard deviations
    F_img3(F_img3<0) = 0; F_img3(F_img3>1) = 1;
    
    F_img = bfilter2(double(F_img3),w,sigma);
   end
axes(handles.axes3)
imshow(F_img)

% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

global F_img
global gImg
global Fsd_Img
%% ============= ETV(Enhance Total Variation) denoising process =====================

lambda = 0.5; mu = 0.4; nu = 0.5; iter = 40; %wdc good psnr with funSSTV
SSTV_DeImg = funETV(im2double(F_img),iter,lambda,mu,nu);

axes(handles.axes4)
imshow(SSTV_DeImg)
[DeNoised_psnr DeNoised_snr] = psnr(im2double(SSTV_DeImg),im2double(gImg));
% C = [num2str(Noisy_psnr),' '];
% set(handles.edit2, 'string', C);
set(handles.edit4,'String',num2str(DeNoised_psnr));
